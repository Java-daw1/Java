package segundaTandaEJ;

public class Ejercicio2 {
    public Ejercicio2() {

        // 2 .- Muestra los números del 320 al 160, contando de 20 en 20 hacia atrás
        // utilizando un bucle while.

        int i = 320;

        while (i >= 160) {
            System.out.println(i);
            i -= 20;
        }
    }
}